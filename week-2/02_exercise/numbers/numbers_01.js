/* 
Create two variables:
numString1 with the value "42".
numString2 with the value "3.14".
Use parseInt() to convert numString1 into an integer.
Use parseFloat() to convert numString2 into a floating-point number.
Print the results using console.log() with messages:
"The integer is 42."
"The floating-point number is 3.14."
*/

// Create two string variables
let numString1 = "42";
let numString2 = "3.14";

// Convert numString1 to an integer using parseInt()
let integerNumber = parseInt(numString1);

// Convert numString2 to a floating-point number using parseFloat()
let floatingPointNumber = parseFloat(numString2);

// Print the results
console.log("The integer is " + integerNumber + ".");
console.log("The floating-point number is " + floatingPointNumber + ".");
